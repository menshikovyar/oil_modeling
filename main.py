# Решение уравнений
t, solution = solve(N1_0, N2_0, N3_0, N1_in, N2_in, T, P, k1, k2, k3, k4, t_end)

# Построение графиков
plot_results(t, solution[:, 0], solution[:, 1], solution[:, 2])

